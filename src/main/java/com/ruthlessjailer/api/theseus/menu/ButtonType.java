package com.ruthlessjailer.api.theseus.menu;

/**
 * @author Vadim Hagedorn
 */
public enum ButtonType {

	/**
	 * No action when clicked.
	 */
	INFO,

	/**
	 * Runs code when clicked.
	 */
	ACTION,

	/**
	 * Gives up its item when clicked.
	 */
	TAKE,

	/**
	 * Receives an item when clicked.
	 * NOT DONE: DO NOT USE.
	 */
	PLACE


}
